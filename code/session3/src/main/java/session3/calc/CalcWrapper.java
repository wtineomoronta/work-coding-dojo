package session3.calc;

public class CalcWrapper {

    public static Double add(Double i, Double i1) {
        return i + i1;
    }

    public static Double subract(Double value, Double value1) {
        return value - value1;
    }

    public static Double multiply(Double value, Double value1) {
        return value * value1;
    }


    public static Double divide(Double value, Double value1) {
        return value / value1;
    }

    public static Double modulus(Double value, Double value1) {
        return value % value1;
    }
}
