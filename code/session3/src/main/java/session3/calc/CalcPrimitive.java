package session3.calc;

public class CalcPrimitive {
    public static int add(int i, int i1) {
        return i + i1;
    }

    public static int subtract(int i, int i1) {
        return i - i1;
    }

    public static int multiply(int i, int i1) {
        return i * i1;
    }

    public static int divide(int i, int i1) {
        return i / i1;
    }

    public static int modulus(int i, int i1) {
        return i % i1;
    }
}
